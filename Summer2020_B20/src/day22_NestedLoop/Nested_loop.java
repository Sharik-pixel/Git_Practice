package day22_NestedLoop;
/*
    write the program that  printing  number from 1 to 5 in new line
 */
public class Nested_loop {
    public static void main(String[] args) {

        for(int i = 1; i <= 5; i++){
            System.out.print(i+" ");
        }
        System.out.println();

        for(int i = 1; i <= 5; i++){
            System.out.print(i+" ");
        }
        System.out.println();

        for(int i = 1; i <= 5; i++){
            System.out.print(i+" ");
        }
        System.out.println();

        for(int i = 1; i <= 5; i++){
            System.out.print(i+" ");
        }
        System.out.println();

        for(int i = 1; i <= 5; i++){
            System.out.print(i+" ");
        }

        System.out.println();

        }
    }
