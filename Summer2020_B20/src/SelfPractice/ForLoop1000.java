package SelfPractice;

public class ForLoop1000 {

    public static void main(String[] args) {
        for(int even = 0; even <= 1000; even++){
            if(even%2==0){
                System.out.println(even+", ");
            }
        }
        System.out.println("======================");
        for(int odd = 0; odd <= 1000; odd++){
            if(odd%2!=0){
                System.out.println(odd+", ");
            }
        }














    }
}
